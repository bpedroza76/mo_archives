        <td height="59" valign="top"><p class="l_text">SOBRE  LA COLECCI&Oacute;N</p>
          <p class="m_text"><img src="../pictures/about1-new.jpg" width="310" height="172" align="left">Los  Archivos del Observatorio de Manila tienen como finalidad preservar cualquier  informaci&oacute;n escrita que contenga trabajo de la instituci&oacute;n para su posterior  uso y difunsi&oacute;n. Nuestra colecci&oacute;n de archivos incluye registros de textos,  fotograf&iacute;as y otros registros de datos de car&aacute;cter hist&oacute;rico y cient&iacute;fico. La  mayor parte del material se obtuvo en el Observatorio de Manila y sus empleados  dirigieron el trabajo de investigaci&oacute;n desde el principio. A pesar de la  destrucci&oacute;n del Observatorio original de Manila durante la Segunda Guerra  Mundial, tenemos la suerte de haber conseguido y guardado la colecci&oacute;n actual  que deja patente la riqueza cient&iacute;fica e hist&oacute;rica de esta instituci&oacute;n.</p>
          <p class="m_text">DESCRIPCI&Oacute;N  Y CLASIFICACI&Oacute;N</p>
          <p class="m_text">La  composici&oacute;n y la clasificaci&oacute;n de toda la colecci&oacute;n (<em>fonds</em>) sigue un sistema basado en los  principios de procedencia y el orden original de los archivos.</p>
          <p class="m_text"><img src="../pictures/about2-new.jpg" width="310" height="194" align="right">La  composici&oacute;n y la clasificaci&oacute;n de toda la colecci&oacute;n (<em>fonds</em>) sigue un sistema basado en los  principios de procedencia y el orden original de los archivos. <a href="spanish_institutional_records.php">Registro Institucional</a>,<a href="spanish_personnel_records.php"> Resgistro de Personal	</a>, <a href="spanish_data_records.php">Registro de Datos</a>, y <a href="spanish_photos_maps.php">Fotos y Mapas</a>. Cada  grupo se divide a su vez en subgrupos y archivos. La finalidad de este esquema  es juntar el material que re&uacute;na las mismas caracter&iacute;sticas por su naturaleza,  el tipo seg&uacute;n su origen (procedencia) y/o el tema. Cada grupo incluye una lista  de archivos con una descripci&oacute;n bibliogr&aacute;fica de cada material sobre el Autor,  el T&iacute;tulo y el A&ntilde;o de Publicaci&oacute;n. A cada archivo se le asigna un C&oacute;digo y un  N&uacute;mero a modo de localizaci&oacute;n. Cada registro contiene una versi&oacute;n digitalizada  de la primera p&aacute;gina para que el usuario pueda visualizar el contenido. Por  favor, dir&iacute;jase al <a href="spanish_site_map.php">Mapa de la web</a> para ver la jerarqu&iacute;a y la composici&oacute;n de  toda la colecci&oacute;n.</p>
          <p class="m_text"> ACCESO</p>
          <p class="m_text">A  parte de la funci&oacute;n <a href="spanish_search.php">B&uacute;squeda</a> de  esta p&aacute;gina, hay disponible una lista de inventario en los Archivos para  orientar al usuario en la localizaci&oacute;n de material espec&iacute;fico de los archivos.</p>
          <p class="m_text">En la  actualidad nos encontramos en la presentaci&oacute;n de las copias digitalizadas de  nuestro registro de archivos. Damos prioridad a los materiales que contengan  datos sin procesar, aquellos que no tienen copias, y aquellos que est&aacute;n da&ntilde;ados  o son m&aacute;s d&eacute;biles. Las versiones digitalizadas disponibles servir&aacute;n para  acceder a las copias del registro original.</p>
