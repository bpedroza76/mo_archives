        <td height="140" valign="top"><p class="l_text">ABOUT THE COLLECTION</p>
          <p class="m_text"> <img src="../pictures/about1-new.jpg" width="310" height="172" align="left">The Manila Observatory Archives aims to preserve, for further use and dissemination, any recorded information that embodies the work of the institution. Our archival collection includes textual records, photographs, and other data records that are historical and scientific in nature. Majority of the materials were produced by the Manila Observatory and its staff in conducting research work since its inception. Despite the destruction of the original Observatory in Manila during World War II, we are fortunate to have acquired and maintained this current collection which chronicles this institution&rsquo;s rich scientific history. </p>
          <p class="m_text">DESCRIPTION AND ARRANGEMENT </p>
          <p class="m_text">The composition and arrangement of the entire collection (<em>fonds</em>) follows a system based on the principles of provenance and original order developed for archives.</p>
          <p class="m_text"><img src="../pictures/about2-new.jpg" width="310" height="194" align="right">The entire Manila Observatory archival collection is divided into four (4) different groups of materials: <a href="institutional_records.php">Institutional records</a>, <a href="personnel_records.php">Personnel records</a>, <a href="data_records.php">Data records</a>, and <a href="photos_maps.php">Photos &amp; Maps</a>. Each group of materials is further divided into sub-groups and their files. The purpose of this scheme is to bring together materials of the same nature and type under the same source (provenance) and/or subject. Each group of materials includes a file list with the pertinent bibliographic description of each material such as the Author, Title and Year of Publication. Each file has an assigned Code and E/F (Envelop/Folder) Number as locators. A digitized version of the first page of each record is provided (except for Biographical records and Correspondence) to provide the user with a preview of its content. Please refer to the Site Map to view the hierarchy and composition of the entire collection. </p>
          <p class="m_text">ACCESS</p>
          <p class="m_text">Aside from the <a href="search.php">Search</a> function of this website, an inventory list is also available in the Archives to guide the user in locating specific archival material. </p>
          <p class="m_text">We are currently in the process of producing digitized copies of our archival records. We are prioritizing materials that contain raw data, those that do not have duplicate copies, and those that are physically weak or damaged. The available digitized versions will serve as the access copies of the original records. </p>
          <p class="m_text">The Manila Observatory Archives is open to all researchers for free, from Monday-Friday, 8 a.m to 5 p.m. All archival materials, in both original and digitized formats, may be accessed within the facility only. The Manila Observatory Archives requires that researchers disclose the purpose of their research before granting access to our archival collection. Written requests and other inquiries can be sent using the address below or by using form found in the <a href="javascript:void(0)" onClick="window.open('http://www.klima.ph/cd4cdm/contact_us/ARCHIVES_contact_us.php','loa','width=800,height=400,scrollbars=no')">Contact Us</a> page. </p>
